# Basic Arithmetic operations

def arithmetic_operations(a, b):
    # Addition
    print("Addition: ", a + b)
    # Subtraction
    print("Subtraction: ", a - b)
    # Multiplication
    print("Multiplication: ", a * b)
    # Division
    print("Division: ", a / b)
    # Modulus
    print("Modulus: ", a % b)
    # Exponentiation
    print("Exponentiation: ", a ** b)


if __name__ == "__main__":
    a = 10
    b = 20
    arithmetic_operations(a, b)